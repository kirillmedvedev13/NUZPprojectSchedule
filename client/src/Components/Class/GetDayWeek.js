export default function GetDayWeek(day_week) {
  switch (day_week) {
    case 1:
      return "Пн";
      break;
    case 2:
      return "Вт";
      break;
    case 3:
      return "Ср";
      break;
    case 4:
      return "Чт";
      break;
    case 5:
      return "Пт";
      break;
    case 6:
      return "Сб";
      break;
  }
}
